#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (s1[a])
		a++;
	while (s2[b] && n--)
		s1[a++] = s2[b++];
	s1[a] = '\0';
	return (s1);
}
