#include "libft.h"

size_t	ft_countwhite(char const *s)
{
	size_t t;

	t = 0;
	while (*s)
	{
		if (*s == ' ' || *s == '\n' || *s == '\t')
			t++;
		s++;
	}
	return (t);
}
